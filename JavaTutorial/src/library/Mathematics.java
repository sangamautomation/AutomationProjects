package library;

public class Mathematics {
	
	public static void multiplicationTable(int n){
		
		for (int i = 1; i <= 10; i++) {
			
			int product = n*i;
			System.out.println(n + " * " + i + " = "+ product);
			}
			
		}
	
	public int add(int a, int b)
	{
		int sum=a+b;
		return sum;
		
	}

	}


